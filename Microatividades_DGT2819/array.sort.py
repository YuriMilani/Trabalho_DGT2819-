# Ordenar a lista de strings 1
my_list = [12, 45, 7, 89, 33, 56, 21, 94, 2, 67, 38, 51, 14, 76, 82]

print("Lista original:", my_list)
my_list.sort()
print("Lista ordenada:", my_list)

# Ordenar a lista em ordem decrescente 

my_list.sort (key=None , reverse=True)
print("Lista ordenada em ordem decrescente:", my_list)

